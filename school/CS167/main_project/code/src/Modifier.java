
/* Class Modifier
 * 
 * just represents a modification to a peptide profile
 */


public class Modifier {

	public int pos;
	public double val;
	
	public Modifier(int pos_t, double val_t) {
		pos = pos_t;
		val = val_t;
	}
	
	public void set(int pos_t, double val_t) {
		pos = pos_t;
		val = val_t;
	}
	
}
